# type() Function
""" type() function is used to find the 
data type of a given variable in python """

# Examples
a = 31
a1 = type(a)            # class <'str'>
print (a1)

b = 12.25
b1 = type(b)            # class <'str'>
print (b1)

name = "Bylal"
name1 = type(name)      # class <'str'>
print (name1)