# LOOPS IN PYTHON
# Loops are used to repeat a block of code multiple times until a condition is met.

# There are mainly two types of loops in Python:
# 1️⃣ for loop  → used when you know how many times to repeat.
# 2️⃣ while loop → used when you want to repeat until a condition becomes False.


# Example
# Used to iterate over a sequence (like list, tuple, string, range).

print("Example: FOR LOOP")
for i in range(5):   # range(5) → 0, 1, 2, 3, 4
    print("Hello", i)
# Output:
# Hello 0
# Hello 1
# Hello 2
# Hello 3
# Hello 4